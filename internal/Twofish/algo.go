func InitTwofish(key string) *Twofish {
	return &Twofish{key: []byte(key)}
}

func (t *Twofish) Encrypt(plaintext string) string {
	keyBytes := expandKey([]byte(t.key))
	plainBytes := []byte(plaintext)

	if len(plainBytes)%16 != 0 {
		padLen := 16 - (len(plainBytes) % 16)
		plainBytes = append(plainBytes, make([]byte, padLen)...)
	}

	result := make([]byte, 0)
	for i := 0; i < len(plainBytes); i += 16 {
		block := plainBytes[i : i+16]
		encrypted := t.encryptBlock(block, keyBytes)
		result = append(result, encrypted...)
	}

	return hex.EncodeToString(result)
}

func (t *Twofish) Decrypt(ciphertext string) string {
	cipherBytes, err := hex.DecodeString(ciphertext)
	if err != nil {
		return ""
	}

	keyBytes := expandKey([]byte(t.key))
	result := make([]byte, 0)

	for i := 0; i < len(cipherBytes); i += 16 {
		block := cipherBytes[i : i+16]
		decrypted := t.decryptBlock(block, keyBytes)
		result = append(result, decrypted...)
	}

	return string(result)
}

func (t *Twofish) encryptBlock(block, key []byte) []byte {
	r := make([]byte, 16)
	copy(r, block)

	for i := 0; i < 16; i++ {
		r[i] ^= key[i]
	}

	for round := 0; round < 16; round++ {
		r0 := uint32(r[0]) | uint32(r[1])<<8 | uint32(r[2])<<16 | uint32(r[3])<<24
		r1 := uint32(r[4]) | uint32(r[5])<<8 | uint32(r[6])<<16 | uint32(r[7])<<24
		r2 := uint32(r[8]) | uint32(r[9])<<8 | uint32(r[10])<<16 | uint32(r[11])<<24
		r3 := uint32(r[12]) | uint32(r[13])<<8 | uint32(r[14])<<16 | uint32(r[15])<<24

		t := r0
		r0 = r0 + r1 + uint32(round)
		r1 = t

		r2 = r2 - r0
		r3 = r3 ^ r1

		r[0] = byte(r0)
		r[1] = byte(r0 >> 8)
		r[2] = byte(r0 >> 16)
		r[3] = byte(r0 >> 24)
		r[4] = byte(r1)
		r[5] = byte(r1 >> 8)
		r[6] = byte(r1 >> 16)
		r[7] = byte(r1 >> 24)
		r[8] = byte(r2)
		r[9] = byte(r2 >> 8)
		r[10] = byte(r2 >> 16)
		r[11] = byte(r2 >> 24)
		r[12] = byte(r3)
		r[13] = byte(r3 >> 8)
		r[14] = byte(r3 >> 16)
		r[15] = byte(r3 >> 24)
	}

	return r
}

func (t *Twofish) decryptBlock(block, key []byte) []byte {
	r := make([]byte, 16)
	copy(r, block)

	for round := 15; round >= 0; round-- {
		r0 := uint32(r[0]) | uint32(r[1])<<8 | uint32(r[2])<<16 | uint32(r[3])<<24
		r1 := uint32(r[4]) | uint32(r[5])<<8 | uint32(r[6])<<16 | uint32(r[7])<<24
		r2 := uint32(r[8]) | uint32(r[9])<<8 | uint32(r[10])<<16 | uint32(r[11])<<24
		r3 := uint32(r[12]) | uint32(r[13])<<8 | uint32(r[14])<<16 | uint32(r[15])<<24

		r3 = r3 ^ r1
		r2 = r2 + r0
		r0 = r0 - r1 - uint32(round)
		r1 = r0 - (r0 - r1)

		r[0] = byte(r0)
		r[1] = byte(r0 >> 8)
		r[2] = byte(r0 >> 16)
		r[3] = byte(r0 >> 24)
		r[4] = byte(r1)
		r[5] = byte(r1 >> 8)
		r[6] = byte(r1 >> 16)
		r[7] = byte(r1 >> 24)
		r[8] = byte(r2)
		r[9] = byte(r2 >> 8)
		r[10] = byte(r2 >> 16)
		r[11] = byte(r2 >> 24)
		r[12] = byte(r3)
		r[13] = byte(r3 >> 8)
		r[14] = byte(r3 >> 16)
		r[15] = byte(r3 >> 24)
	}

	for i := 0; i < 16; i++ {
		r[i] ^= key[i]
	}

	return r
}

func expandKey(key []byte) []byte {
	if len(key) < 16 {
		newKey := make([]byte, 16)
		copy(newKey, key)
		key = newKey
	}

	if len(key) > 16 {
		key = key[:16]
	}

	if len(key) < 16 {
		for i := len(key); i < 16; i++ {
			key = append(key, 0)
		}
	}

	return key
}

func gfunc(x uint32, key []byte) uint32 {
	b0 := byte(x)
	b1 := byte(x >> 8)
	b2 := byte(x >> 16)
	b3 := byte(x >> 24)

	y0 := q0[b0] ^ key[0]
	y1 := q1[b1] ^ key[1]
	y2 := q0[b2] ^ key[2]
	y3 := q1[b3] ^ key[3]

	return uint32(y0) | uint32(y1)<<8 | uint32(y2)<<16 | uint32(y3)<<24
}

