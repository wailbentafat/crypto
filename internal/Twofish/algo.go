type TwofishCipher struct {
	key []byte
}

func NewTwofishCipher(key []byte) (cipher.Block, error) {
	if len(key) != 16 && len(key) != 24 && len(key) != 32 {
		return nil, fmt.Errorf("invalid key size: %d", len(key))
	}
	return &TwofishCipher{key: key}, nil
}

func (c *TwofishCipher) BlockSize() int {
	return 16
}

func (c *TwofishCipher) Encrypt(dst, src []byte) {
	t := InitTwofish(string(c.key))
	encrypted := t.encryptBlock(src, expandKey(c.key))
	copy(dst, encrypted)
}

func (c *TwofishCipher) Decrypt(dst, src []byte) {
	t := InitTwofish(string(c.key))
	decrypted := t.decryptBlock(src, expandKey(c.key))
	copy(dst, decrypted)
}