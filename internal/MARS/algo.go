package mars

import (
	"encoding/hex"
)

type MARS struct {
	key []byte
	rk  []uint32
}

func InitMARS(key string) *MARS {
	m := &MARS{key: []byte(key)}
	m.keySchedule()
	return m
}

func (m *MARS) Encrypt(plaintext string) string {
	plainBytes := []byte(plaintext)

	if len(plainBytes)%16 != 0 {
		padLen := 16 - (len(plainBytes) % 16)
		plainBytes = append(plainBytes, make([]byte, padLen)...)
	}

	result := make([]byte, 0)
	for i := 0; i < len(plainBytes); i += 16 {
		block := plainBytes[i : i+16]
		encrypted := m.encryptBlock(block)
		result = append(result, encrypted...)
	}

	return hex.EncodeToString(result)
}

func (m *MARS) Decrypt(ciphertext string) string {
	cipherBytes, _ := hex.DecodeString(ciphertext)
	result := make([]byte, 0)

	for i := 0; i < len(cipherBytes); i += 16 {
		block := cipherBytes[i : i+16]
		decrypted := m.decryptBlock(block)
		result = append(result, decrypted...)
	}

	return string(result)
}

func (m *MARS) keySchedule() {
	m.rk = make([]uint32, 32)

	if len(m.key) < 16 {
		newKey := make([]byte, 16)
		copy(newKey, m.key)
		m.key = newKey
	} else if len(m.key) > 16 {
		m.key = m.key[:16]
	}

	for i := 0; i < 4; i++ {
		m.rk[i] = uint32(m.key[i*4]) | uint32(m.key[i*4+1])<<8 |
			uint32(m.key[i*4+2])<<16 | uint32(m.key[i*4+3])<<24
	}

	for i := 4; i < 32; i++ {
		m.rk[i] = m.rk[i-4] + (m.rk[i-1] ^ m.rk[i-3]) + 0x87DEADB6
	}
}

func (m *MARS) encryptBlock(block []byte) []byte {
	var a, b, c, d uint32

	a = uint32(block[0]) | uint32(block[1])<<8 | uint32(block[2])<<16 | uint32(block[3])<<24
	b = uint32(block[4]) | uint32(block[5])<<8 | uint32(block[6])<<16 | uint32(block[7])<<24
	c = uint32(block[8]) | uint32(block[9])<<8 | uint32(block[10])<<16 | uint32(block[11])<<24
	d = uint32(block[12]) | uint32(block[13])<<8 | uint32(block[14])<<16 | uint32(block[15])<<24

	a += m.rk[0]
	b += m.rk[1]
	c += m.rk[2]
	d += m.rk[3]

	for i := 4; i < 20; i++ {
		t := a + m.rk[i]
		t = m.sbox(t)
		b ^= t
		t = b + m.rk[i+1]
		t = m.sbox(t)
		c ^= t
		t = c + m.rk[i+2]
		t = m.sbox(t)
		d ^= t
		t = d + m.rk[i+3]
		t = m.sbox(t)
		a ^= t

		if i&1 == 0 {
			a, b, c, d = b, c, d, a
		}
	}

	a += m.rk[20]
	b += m.rk[21]
	c += m.rk[22]
	d += m.rk[23]

	a, b, c, d = d, a, b, c

	for i := 24; i < 28; i++ {
		t := a + m.rk[i]
		t = m.sbox(t)
		b ^= t
		t = b + m.rk[i+1]
		t = m.sbox(t)
		c ^= t
	}

	result := make([]byte, 16)
	result[0] = byte(a)
	result[1] = byte(a >> 8)
	result[2] = byte(a >> 16)
	result[3] = byte(a >> 24)
	result[4] = byte(b)
	result[5] = byte(b >> 8)
	result[6] = byte(b >> 16)
	result[7] = byte(b >> 24)
	result[8] = byte(c)
	result[9] = byte(c >> 8)
	result[10] = byte(c >> 16)
	result[11] = byte(c >> 24)
	result[12] = byte(d)
	result[13] = byte(d >> 8)
	result[14] = byte(d >> 16)
	result[15] = byte(d >> 24)

	return result
}

func (m *MARS) decryptBlock(block []byte) []byte {
	var a, b, c, d uint32

	a = uint32(block[0]) | uint32(block[1])<<8 | uint32(block[2])<<16 | uint32(block[3])<<24
	b = uint32(block[4]) | uint32(block[5])<<8 | uint32(block[6])<<16 | uint32(block[7])<<24
	c = uint32(block[8]) | uint32(block[9])<<8 | uint32(block[10])<<16 | uint32(block[11])<<24
	d = uint32(block[12]) | uint32(block[13])<<8 | uint32(block[14])<<16 | uint32(block[15])<<24

	b, c, d = d, a, b

	a -= m.rk[20]
	b -= m.rk[21]
	c -= m.rk[22]
	d -= m.rk[23]

	for i := 24; i < 28; i++ {
		t := a + m.rk[i]
		t = m.sbox(t)
		b ^= t
		t = b + m.rk[i+1]
		t = m.sbox(t)
		c ^= t
	}

	for i := 19; i >= 4; i-- {
		if i&1 == 0 {
			a, b, c, d = d, a, b, c
		}

		t := d + m.rk[i+3]
		t = m.sbox(t)
		a ^= t
		t = c + m.rk[i+2]
		t = m.sbox(t)
		b ^= t
		t = b + m.rk[i+1]
		t = m.sbox(t)
		c ^= t
		t = a + m.rk[i]
		t = m.sbox(t)
		d ^= t
	}

	a -= m.rk[0]
	b -= m.rk[1]
	c -= m.rk[2]
	d -= m.rk[3]

	result := make([]byte, 16)
	result[0] = byte(a)
	result[1] = byte(a >> 8)
	result[2] = byte(a >> 16)
	result[3] = byte(a >> 24)
	result[4] = byte(b)
	result[5] = byte(b >> 8)
	result[6] = byte(b >> 16)
	result[7] = byte(b >> 24)
	result[8] = byte(c)
	result[9] = byte(c >> 8)
	result[10] = byte(c >> 16)
	result[11] = byte(c >> 24)
	result[12] = byte(d)
	result[13] = byte(d >> 8)
	result[14] = byte(d >> 16)
	result[15] = byte(d >> 24)

	return result
}

func (m *MARS) sbox(x uint32) uint32 {
	m0 := (x >> 24) & 0xFF
	m1 := (x >> 16) & 0xFF
	m2 := (x >> 8) & 0xFF
	m3 := x & 0xFF

	s0 := sbox0[m0]
	s1 := sbox1[m1]
	s2 := sbox2[m2]
	s3 := sbox3[m3]

	return uint32(s0)<<24 | uint32(s1)<<16 | uint32(s2)<<8 | uint32(s3)
}

